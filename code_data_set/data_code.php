<h1>Data sets and Code section</h1>

<h2>Introduction</h2>
The aim of this web page is to provide data sets and elementary codes
to researchers working on discrete geometry. The idea is to make it
easy for the community to try our own algorithms on the same image
examples as other researchers, as well as to try the algorithms of
other researchers.
Several sections are presented in this web page:
<ul>
  <li><a href="https://www.tc18.org/code_data_set/data.php">Data sets</a>: 2D and 3D images (binary,
grayscale,...)
  </li>
  <li> <a href="https://www.tc18.org/code_data_set/demos.php"> Demos</a>: online demonstrators of DG
  algorithms and applications.
  </li>
  <li> <a href="https://www.tc18.org/code_data_set/code.php"> Code Repository</a>: simple codes from
researchers to researchers.
  </li>
  <li> <a href="https://www.tc18.org/code_data_set/tools.php"> Tools avaliable on the web</a>: links to
useful tools.
  </li>
  <li> <a href="#submit"> Submit your own data/code</a>: how to submit
a code ? How to submit the results of your own algorithm on our data
sets ?...
  </li>
</ul>
Obviously, feel free to send an e-mail to authors of these web pages
(see below)
if you have comments.
