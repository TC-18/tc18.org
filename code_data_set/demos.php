<h2> IPOL </h2>
<a href="www.ipol.im">Image Processing Online</a> is "a research journal of image processing and
image analysis which emphasizes the role of mathematics as a source for
algorithm design and the reproducibility of the research". An online
demo is attached to each article published in this journal.
Digital geometry demos are under the topic "Geometry" and are listed
below.
<br>
<br>

