    <h2><a name="formembers">For members</a></h2>
    
    <p>
      An electronic newsletter is sent to the members two or three times every year. It presents a summary of the activities and upcoming events.
    </p>

    <p>
      During each <a href="events.html#dgci">DGCI</a> (Discrete Geometry for Computer Imagery - main conference for TC18), a TC meeting is held to give the possibility for the members to discuss the activity of the TC. 
    </p>

    <p>
      Suggestion on the activity of TC18 and modifications of the TC18 web page are warmly welcome.
    </p>

