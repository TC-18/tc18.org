    <h2> What is TC18 ?</h2>

    <p> The aim of this IAPR-Technical Committee is to promote
      interactions and collaboration between researchers working on
      Discrete Geometry and Mathematical Morphology. Conferences and a
      TC web page provide these opportunities. The topic is not
      covered by other IAPR-TCs, but has links with some of them.
      </p>

    <p>
      The main conferences are the Discrete Geometry for Computer Imagery (DGCI) conference  and  International Symposium on Mathematical Morphology (ISMM). DGCI is a series of conferences of which the first was held in 1991 in Strasbourg, and now held every 18 months. It is an international conference by its participants, its scientific committee and its location. Since the 6th DGCI (November 1996), proceedings have been published by Springer Verlag, in the Lecture Notes in Computer Science series. ISMM started two years after DGCI in Barcelona and held every two years and its proceedings are also published in LNCS series. More details about the history of these two conferences are given in the <a href="http://www.tc18.org/events.php">event page</a>.
    </p>

    <p>
      In combination with each DGCI, a TC meeting is held to give the possibility for the members to discuss the activity of the TC. An electronic newsletter is sent regularly to the members.
    </p>

    <p>
      The TC18 web page is maintained in order to access easily
      information on on-going research for researchers active within the
      field, as well as information on basic notions for researchers in
      related fields, or interested in starting an activity in discrete
      geometry. You will find a lot of information on this web page, so
      have a nice visit !

