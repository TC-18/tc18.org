<h2><a class="name" name="subfields_and_related_lectures">Subfields and related
lectures <br>
</a></h2>
Discrete geometry can be structured into several subfields. Of course
this kind of classification is not unique... we proposed below a
structure as logical as possible in the following. For each subfield,
we expect to propose (as soon as possible...) some main references,
lectures or slides provided by members and links to data sets and codes.<br>
<br>
<ul>
  <li><a href="subfields/topology/index.php">Grids, cells, structures
and topology</a></li>
 digital topology, border/frontier tracking, graph theory and combinatorics<br>
</ul>
<ul>
  <li><a href="subfields/nD_objects/index.php">nD objects and
recognition</a></li>
 digitization frameworks and models,  discrete analytical objects (lines, planes, circles,
...), recognition algorithms<br>
</ul>
<ul>
  <li><a href="subfields/distance_skeletons/index.php">Discrete
distances and skeletons</a></li>
square and diamond distances, chamfer distances, euclidean distance,
skeletons, medial axis and medial lines/surfaces<br>
</ul>
<ul>
  <li><a href="subfields/geometrical_transforms/index.php">Discrete
geometrical transforms </a></li>
rotations, magnification<span style="text-decoration: underline;"></span>
</ul>
<ul>
  <li><a href="subfields/shape_representation/index.php">Various shape
representation </a></li>
hulls, multiscale shapes, Fourier moments, fractals, ...<br>
</ul>
<ul>
  <li><a href="subfields/features_extraction/index.php">Features
extraction and similarity measures</a></li>
convexity, lengths and areas, curvature, normals, ...<br>
</ul>
<ul>
  <li><a href="subfields/applications/index.php">Examples of
applications</a></li>
image rendering, compression, ...
</ul>
Do not hesitate to help us to complete these subfields. Thanks in
advance!<br>
